const crimevalid_results = document.querySelector(".crimevalid_results");
const crimecode_input = document.getElementById("crimecode_input");

crimecode_input.onkeyup = function(){
    let result = []; 
    let input = crimecode_input.value; //filter
    if(input.length){
        result = availableKeywords.filter((keyword)=>{
            return keyword.toLowerCase().includes(input.toLowerCase());
        });
        console.log(result);
    }
    display(result);
    if(!result.length){
        crimevalid_results.innerHTML = '';
    }
}

function display(result){
    const content = result.map((list)=>{
        return "<li onclick=selectInput(this)>" + list + "</li>";
    });
    crimevalid_results.innerHTML = "<ul>" + content.join('') + "</ul>";
}

function selectInput(list){
    crimecode_input.value = list.innerHTML;
    crimevalid_results.innerHTML = '';
}